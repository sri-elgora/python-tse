from wormtypes import *

class WormException(Exception):
    def __init__(self, errno, message):
        self.errno = errno
        self.message = message

    def __repr__(self):
        return '%s: %s' % (self.errno, self.message)


def WormError_to_exception(errno):
    if errno == WORM_ERROR_NOERROR:
        return None
    raise WormException(errno, errno_map.get(errno, 'WORM_ERROR_UNKNOWN (0x%04X)' % errno))


